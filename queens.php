                            <br>
                            Este Draft se celebró el 16 de abril de 2023 en el Cupra Arena. El photocall tuvo lugar a
                            las 16:15h y la selección de las jugadoras a las 18h. En cuanto al inicio de la competición,
                            comenzará el sábado 6 de mayo. Pese a que todavía no hay una hora de inicio concretada, se
                            espera que sea el mismo horario que el de la Kings League, por lo que debería empezar a las
                            16h.
                        </p>

                        <p class="justify-text">
                            En el caso de organizarse algún evento preliminar para conmemorar el arranque de la Queens
                            League, la competición podría arrancar antes de las 16h. Tanto la fase de grupos como las
                            eliminatorias posteriores se disputarán los sábados.

                            El formato de la Queens League consta de 11 jornadas, disputándose seis partidos seguidos
                            cada sábado por la tarde. Serán encuentros de fútbol 7 con una corta duración, de 40
                            minutos, con unas reglas novedosas cuyo objetivo principal es el de ofrecer espectáculo. Al
                            finalizar la competición, los 8 mejores equipos clasificados disputarán el playoff.
